let dia=parseInt(prompt("Digite um número entre um a sete para descobrir o dia da semana"));

switch(dia){
    case 1: alert("É domingo.")
    break;
    case 2: alert("É segunda.")
    break;
    case 3: alert("É terça.")
    break;
    case 4: alert("É quarta.")
    break;
    case 5: alert("É quinta.")
    break;
    case 6: alert("É sexta.")
    break;
    case 7: alert("É sabado.")
    break;
    default: alert("Número inválido.")
}